// push() insere elemento no final do array
// útil parar iserir elemento sem precisar saber o tamanho do array
const arr = []; // funcionam em constante
console.log(arr);

arr.push(1);
console.log(arr);


arr.push(10);
console.log(arr);

arr.push(100);
console.log(arr);


// pop() remove e retorna o último elemento do array
console.log(arr.pop());
console.log(arr);

console.log(arr.pop());
console.log(arr);